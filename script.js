  let index = 0;
  
  function nextParagraph() {
    const storyBlock = document.getElementById("story");
    if (index < story.length) {
      storyBlock.style.opacity = 0;
      setTimeout(() => {
        storyBlock.textContent = story[index];
        storyBlock.style.opacity = 1;
        index++;
      }, 300);
    } else {
      storyBlock.textContent = "Конец сказки.";
    }
  }